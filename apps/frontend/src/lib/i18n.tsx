type Vars = Record<string, string | number>;

type LangCtx = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, vars?: Vars) => string;
};

const Ctx = createContext<LangCtx>({
  lang: 'en',
  setLang: () => {},
  t: (k) => k,
});

export function LangProvider({ children }: { children: ReactNode }) {
  const { user, setLanguage } = useAuth();
  // Initialize from the user profile when it loads.
  const [lang, setLangLocal] = useState<Lang>('en');
  useEffect(() => { if (user?.language) setLangLocal(user.language); }, [user?.language]);

  const setLang = (l: Lang) => {
    setLangLocal(l);
    if (user) setLanguage(l);
    document.documentElement.setAttribute('lang', l);
  };

  const t = (key: string, vars?: Vars) => {
    let s = (I18N[lang] && I18N[lang][key]) || I18N.en[key] || key;
    if (vars) for (const k of Object.keys(vars)) s = s.replace('{' + k + '}', String(vars[k]));
    return s;
  };

  // Mirror the localised generic-error fallback into window so non-React
  // modules (lib/errorToast.ts) can surface translated copy.
  useEffect(() => {
    if (typeof window !== 'undefined') {
      window.__genericErrorMessage = t('generalErrorMsg');
    }
  }, [lang]);

  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>;
}

export function useT() {
  return useContext(Ctx);
}
